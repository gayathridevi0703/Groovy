class Datatype{
    static void main(String[] args){
        int a=10;

        long b=200l;

        float c=50.70f;

        double d=20.10e34;

        BigInteger e=10000g;

        BigDecimal f=200.00g;

        println("int is "+a);
        println("long is "+b);
        println("float is "+c);
        println("double is "+d);
        println("BigInteger is "+e);
        println("BigDecimal is "+f);
     
        

    }
}